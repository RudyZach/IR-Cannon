    public int[] getCommand(String command, String brand) {
        //loop through to find command index
        int index = 0;
        for (int i = 0; i < funcs.length; i++) {
            if (command.equalsIgnoreCase(funcs[i])) {
                index = i;
                break;
            }
        }

        int[] ans;
        switch (brand) {
            case "NEC":
                ans = NEC.get(index);
                break;
            case "EPSON":
                ans = Epson.get(index);
                break;
            case "SAMSUNG":
                ans = Samsung.get(index);
                break;
            default:
                ans = NEC.get(index);
                break;
        }

        return ans;
    }

